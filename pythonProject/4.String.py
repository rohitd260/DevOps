name =  "python"
'''String function in action'''
# 1.len function : To print or find length of string
print(len(name))
# 2. Indexing function : To print value at a index od string
print(name[0])  #print character at index 0 or first place
print(name[-1])  #print character at last index

#3. Slicicng function
print(name[0:len(name)])  #prints the name
print(name[1:4])  #prints the 'yth'
print(name[::-1]) #prints the 'reverse string'
print(name[::-2])  #prints the reverse charcter by skipping 1 index i.e reverse is nohtyp gives nhy